#include <stdio.h>

int main(void){

        char a;
        scanf("%c", &a);
        printf("char: %c\n", a);
        
        int b;
        scanf("%d", &b);
        printf("int: %d\n", b);
        
        long c;
        scanf("%ld", &c);
        printf("long: %ld\n", c);
        
        float d;
        scanf("%f", &d);
        printf("float: %f\n", d);
        
        double e;
        scanf("%lf", &e);
        printf("double: %lf\n", e);

        
        return 0;
}
